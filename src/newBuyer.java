    private void jTextField2FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField2FocusLost
        // TODO add your handling code here:
    String contactNo = jTextField2.getText().trim();
    
    // Check if the text field is empty
    if (contactNo.isEmpty()) {
        jTextField2.setText("Enter Contact No.");
        jTextField2.setForeground(new Color(153, 153, 153));
    } else {
        // Validate the contact number to allow only 10-digit numbers
        try {
            long number = Long.parseLong(contactNo);
            if (String.valueOf(number).length() != 10) {
                JOptionPane.showMessageDialog(null, "Please enter a valid 10-digit contact number.");
                jTextField2.setText("");  // Clear the text field
                jTextField2.requestFocus();  // Set focus back to the text field
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please enter a valid 10-digit contact number.");
            jTextField2.setText("");  // Clear the text field
            jTextField2.requestFocus();  // Set focus back to the text field
        }
    }
    }//GEN-LAST:event_jTextField2FocusLost

