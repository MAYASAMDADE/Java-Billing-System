    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    String name = jTextField1.getText();
    String contactNo = jTextField2.getText();
    String email = jTextField3.getText();
    String address = jTextField4.getText();
    String regdate = jLabel7.getText();  // Assuming jLabel7 contains the date as a String

    try {
        Connection con = ConnectionProvider.getCon();

        // Check if the connection is successful
        if (con != null) {
            String insertQuery = "INSERT INTO customer (name, contactNo, email, address, regdate) VALUES (?, ?, ?, ?, ?)";

            try (PreparedStatement pst = con.prepareStatement(insertQuery)) {
                pst.setString(1, name);
                pst.setString(2, contactNo);
                pst.setString(3, email);
                pst.setString(4, address);

                // Convert regdate String to java.sql.Date
                SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
                java.util.Date parsedDate = dateFormat.parse(regdate);
                java.sql.Date sqlDate = new java.sql.Date(parsedDate.getTime());

                pst.setDate(5, sqlDate);

                pst.executeUpdate();
            }

            JOptionPane.showMessageDialog(null, "Successfully Updated");
            setVisible(false);
            new NewDevice().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Failed to connect to the database");
        }
        
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Mobile NO. is already Exist or an error occurred");
        e.printStackTrace();  // Print the stack trace for debugging purposes
    }

    }//GEN-LAST:event_jButton1ActionPerformed

