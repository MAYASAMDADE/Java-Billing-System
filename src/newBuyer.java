    private void jTextField3FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField3FocusLost
        // TODO add your handling code here:
        String email = jTextField3.getText().trim();
    
    // Check if the text field is empty
    if (email.isEmpty()) {
        jTextField3.setText("Enter Email");
        jTextField3.setForeground(new Color(153, 153, 153));
    } else {
        // Validate the email to require "@gmail.com" at the end
        if (!isValidEmail(email)) {
            JOptionPane.showMessageDialog(null, "Please enter a valid email address ending with '@gmail.com'.");
            jTextField3.setText("");  // Clear the text field
            jTextField3.requestFocus();  // Set focus back to the text field
        }
    }
    }//GEN-LAST:event_jTextField3FocusLost

    private boolean isValidEmail(String email) {
    // Use a regular expression to validate the email address
    String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@gmail\\.com$";
    return email.matches(emailRegex);
}
    
    
   
    
