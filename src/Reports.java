    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
       try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd", Locale.getDefault());
            String startDate = sdf.format(jDate3.getDate());
            String endDate = sdf.format(jDate4.getDate());

            int selectedIndex = jComboBox1.getSelectedIndex();
            String query;

            switch (selectedIndex) {
                case 1:
                    query = "SELECT * FROM fdevice WHERE date(DateReceived) BETWEEN ? AND ? AND DeviceType='Smartphone'";
                    break;
                case 2:
                    query = "SELECT * FROM fdevice WHERE date(DateReceived) BETWEEN ? AND ? AND DeviceType='Tablet'";
                    break;
                case 3:
                    query = "SELECT * FROM fdevice WHERE date(DateReceived) BETWEEN ? AND ?";
                    break;
                default:
                    query = "SELECT * FROM fdevice";
                    break;
            }

            try (PreparedStatement pst = con.prepareStatement(query)) {
                pst.setString(1, startDate);
                pst.setString(2, endDate);

                try (ResultSet resultSet = pst.executeQuery()) {
                    DefaultTableModel tblmodel = (DefaultTableModel) jTable2.getModel();
                    tblmodel.setRowCount(0); // Clear existing rows

                    while (resultSet.next()) {
                        String id = Integer.toString(resultSet.getInt("DeviceID"));
                        String deviceType = resultSet.getString("DeviceType");
                        String brand = resultSet.getString("Brand");
                        String model = resultSet.getString("Model");
                        String imeiSerialNumber = resultSet.getString("IMEISerialNumber");
                        String repairHistory = resultSet.getString("RepairHistory");
                        String deviceCondition = resultSet.getString("DeviceCondition");
                        String dateReceived = resultSet.getString("DateReceived");
                        String notes = resultSet.getString("Notes");
                        String customerContactNo = resultSet.getString("CustomerContactNo");

                        String[] rowData = {id, deviceType, brand, model, imeiSerialNumber, repairHistory, deviceCondition, dateReceived, notes, customerContactNo};
                        tblmodel.addRow(rowData);
                    }
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "SQL Error: " + e.getMessage());
            e.printStackTrace();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    
    }//GEN-LAST:event_jButton3ActionPerformed

