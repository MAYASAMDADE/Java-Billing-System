    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
         try {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd", Locale.getDefault());
        String d = sdf.format(jDate5.getDate());
        String d1 = sdf.format(jDate6.getDate());
        System.out.println(d + " , " + d1);

        PST = con.prepareStatement("SELECT * FROM inventary WHERE DATE(indate) >= ? AND DATE(indate) <= ?");
        PST.setString(1, d);
        PST.setString(2, d1);

        r = PST.executeQuery();
        DefaultTableModel tblmodel = (DefaultTableModel) jTable3.getModel();
        tblmodel.setRowCount(0); // Clear existing rows in the table

        while (r.next()) {
            String indate = r.getString("indate");
            String id = Integer.toString(r.getInt("iId"));
            String brand = r.getString("brand");
            String category = r.getString("category");
            int stock = r.getInt("stock");
            int remainingStock = r.getInt("remainingstock");
            int rate = r.getInt("rate");

            String[] rowData = {indate, id, brand, category, Integer.toString(stock), Integer.toString(remainingStock), Integer.toString(rate)};
            tblmodel.addRow(rowData);
        }
    } catch (SQLException ex) {
        Logger.getLogger(Reports.class.getName()).log(Level.SEVERE, null, ex);
    }

    }//GEN-LAST:event_jButton5ActionPerformed

